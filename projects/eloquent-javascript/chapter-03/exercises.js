// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  let math = Math.min(num1, num2);
  return math
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  let answer = false;
  if (num % 2 === 0) {
    answer = true;
  } 
  return answer
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
  let count = [];
  for (let x = 0; x < string.length; x++) {
    if (string[x] === letter) {
      count.push(string[x]);
    }
  }
  return count.length
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count = [];
  for (let x = 0; x < string.length; x++) {
    if (string[x] === 'B') {
      count.push(string[x]);
    }
  }
  return count.length
}

